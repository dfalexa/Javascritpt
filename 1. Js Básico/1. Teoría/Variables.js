// CLASE 4. Qué es una variable en JavaScript

// Una varibale es la representación de un lugar que reservamos en memoria para guardar un valor. El valor puede ser cualquier tipo de dato, inclusive objetos o funciones.
var nombre = "Oscar"; // var es la palabra reservada para guardar un valor, el espacio en la memoria llamada nombre, cuando quiera acceder el valor de nombre tengo que llamarla por "nombre". El ; indica una buena práctica y dice que ya terminamos una sentencia.

// Existen dos estados de una variable
// 1. Declarar una variable:
var edad; // reserva el espacio de la variable, y después le damos el valor. 

// 2. Inicializar 
edad = 30; // la variable ya fue declarada e inicializada, es decir ya tiene un valor y ya la puede utilizar.
edad

// Variables con array:
var elementos = ["Computadora", "Celular"];

// Variable tipo objeto:
var persona ={
    nombre: "Diego", // la coma (,) es sintaxis de generar un objeto
    edad: 30
}

persona;




